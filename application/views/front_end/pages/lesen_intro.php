<p><strong>Pengantar ujian membaca (lesen)</strong></p>

<p><span style="line-height: 1.6em;">Ujian membaca &nbsp;pada sertifikasi A1 Bahasa Jerman teridiri dari 15 soal dan meliputi tiga bagian:</span></p>

<p><strong>Bagian pertama (teil &nbsp;1)</strong></p>

<p>Pada bagian pertama perserta akan berhadapan dengan bacaan (biasanya berupa surat) yang diikuti oleh beberapa pernyataan. Pertanyaan &ndash; pernyataan tersebut bernilai benar jika sesuai dengan isi surat. Dan bernilai salah jika tidak sesuai atau bertentangan dengan isi surat. Satu bacaan surat biasanya terdiri dari 2 &ndash; 3 pernyataan yang berbeda.</p>

<p><strong>Bagian kedua (teil 2)</strong></p>

<p>Pada bagian kedua, peserta ujian akan mendapatkan sebuah pertanyaan, kemudian memilih pilihan jawaban berupa pengumuman yang sesuai dengan pertanyaan yang diajukan. Pada bagian kedua setiap pilihan jawaban disertai dengan gambar pengumuman, sehingga peserta harus membandingkan dan memilih mana pilihan pengumuman sesuai dengan pertanyaan.</p>

<p><strong>Bagian ketiga (teil 3)</strong></p>

<p>Pada bagian ketiga, peserta akan dihadapkan sebuah gambar pengumuman/poster. Gambar tersebut berisi informasi yang kemudian diikuti oleh sebuah pernyataan. Pernyataan tersebut bernilai benar jika sesuai dengan isi pengumuman dan begitu juga sebaliknya, bernilai salah jika tidak sesuai dengan pernyataan.&nbsp;</p>
