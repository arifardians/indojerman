<p><strong>Pengantar Ujian Mendengarkan (H&ouml;ren)</strong></p>

<p>Ujian mendengarkan pada sertifikasi A1 Bahasa Jerman teridiri dari 15 soal dan meliputi tiga bagian:</p>

<p><strong>1. Bagian pertama (teil 1)</strong></p>

<p>Pada bagian pertama, peserta ujian akan mendengarkan percakapan singkat antara dua orang. Selanjutnya akan diajukan pertanyaan seputar isi pembicaraan yang di dengarkan. Pada bagian pertama pilihan jawaban mempunyai gambar, sehingga membantu peserta ujian untuk menebak topik pembicaraan sebelum dialog diperdengarkan. &nbsp;Jumlah soal 4-5.</p>

<p><strong>2. Bagian kedua (teil 2)</strong></p>

<p>Pada bagian kedua, peserta akan mendengarkan pengumuman yang berupa monolog. Dari pengumuman yang didengar, akan terdapat sebuah pernyataan yang diajukan kepada peserta. Jika pernyataan sesuai dengan isi pengumuman, maka nilainya benar (richtig). Sebaliknya jika pernyataan tidak sesuai dengan isi pengumuman, maka nilainya salah (falsch). Jumlah soal 5.</p>

<p><strong>3. Bagian ketiga (teil 3)</strong></p>

<p>Pada bagian ketiga, peserta juga akan mendengarkan pengumuman yang berupa monolog. Dari pengumuman yang di dengar akan diajukan sebuah pertanyaan. Pada bagian ini akan terdapat beberapa pilihan jawaban tanpa disertai dengan gambar. &nbsp;Jumlah soal 5.</p>

